using UnityEngine;
using System.Collections;

public class FadeAnimation : MonoBehaviour
{
    public FadeAnimation Instance { get; private set; }
    private void Awake()
    {
        if (Instance == null)
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}
